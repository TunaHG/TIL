# 1. Arithmetic Operator : 산술 연산자 ----
# +, -, *, /, **, ^, %/%, %%
3 + 4     # 더하기
3 - 4     # 빼기
3 * 4     # 곱하기
3 / 4     # 나누기
3 ** 4    # 거듭제곱
3 ^ 4     # 거듭제곱
13 %/% 4  # 몫
